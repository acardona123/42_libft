#include "../includes/libft.h"

int	ft_isprint(int c)
{
	if (c < 32 || c > 126)
		return (0);
	if (c == 32)
		return (64);
	if (c >= '0' && c <= '9')
		return (4);
	if (c >= 'A' && c <= 'Z')
		return (1);
	if (c >= 'a' && c <= 'z')
		return (2);
	return (16);
}
